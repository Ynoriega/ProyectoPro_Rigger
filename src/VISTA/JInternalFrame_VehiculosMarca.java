public void GuardarMarca()
{
    try
    {
        if(Txt_Maraca_Vehiculo.getText().isEmpty() || Txt_Marca_Moto.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Error no deben quedar campos vacios");
        }
        else
        {
        obj.GuardarMarca(Txt_Maraca_Vehiculo.getText(), Txt_Marca_Moto.getText());
        JOptionPane.showMessageDialog(null, "DATOS REGISTRADOS CORRECTAMENTE","GUARDAE",JOptionPane.INFORMATION_MESSAGE);
        this.Limpiar();
        }
    }
    catch(Exception ex)
    {
        System.out.println("Error " + ex.getMessage());
    }
}
