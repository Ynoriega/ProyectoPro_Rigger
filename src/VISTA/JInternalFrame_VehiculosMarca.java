    private void Txt_Maraca_VehiculoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Txt_Maraca_VehiculoKeyTyped
        char c = evt.getKeyChar();

        if((c<'a' || c>'z') && (c<'A' || c>'Z') && (c<'!' || c>'.')
            && (c!=(char)KeyEvent.VK_SPACE))
        {
            evt.consume();//CODIGO PARA INGRESAR SOLO LETRAS
        } 
        else if(Character.isLowerCase(c))//CODIGO PARA FORZAR LA MAYUSCULAS
        {
            String cadena = (""+c).toUpperCase();
            c=cadena.charAt(0);
            evt.setKeyChar(c);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_Txt_Maraca_VehiculoKeyTyped

